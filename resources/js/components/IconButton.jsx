import { Link } from 'react-router-dom';

const variants = {
    default: 'text-slate-600 hover:bg-slate-100 hover:text-slate-900',
    primary: 'text-blue-800 hover:bg-blue-50 hover:text-blue-900',
    success: 'text-green-700 hover:bg-green-50 hover:text-green-900',
    warning: 'text-amber-700 hover:bg-amber-50 hover:text-amber-900',
    danger: 'text-red-600 hover:bg-red-50 hover:text-red-900',
};

export default function IconButton({
    title,
    onClick,
    href,
    externalHref,
    children,
    variant = 'default',
    disabled = false,
}) {
    const className = `inline-flex items-center justify-center w-9 h-9 rounded-lg transition-colors disabled:opacity-40 disabled:cursor-not-allowed ${variants[variant]}`;

    if (externalHref) {
        return (
            <a
                href={externalHref}
                target="_blank"
                rel="noopener noreferrer"
                title={title}
                className={className}
                aria-label={title}
            >
                {children}
            </a>
        );
    }

    if (href) {
        return (
            <Link to={href} title={title} className={className} aria-label={title}>
                {children}
            </Link>
        );
    }

    return (
        <button
            type="button"
            title={title}
            aria-label={title}
            onClick={onClick}
            disabled={disabled}
            className={className}
        >
            {children}
        </button>
    );
}

